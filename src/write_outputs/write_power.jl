@doc raw"""
	write_power(path::AbstractString, inputs::Dict, setup::Dict, EP::Model)

Function for writing the different values of power generated by the different technologies in operation.
"""
function write_power(path::AbstractString, inputs::Dict, setup::Dict, EP::Model)
    gen = inputs["RESOURCES"]
    zones = zone_id.(gen)

    G = inputs["G"]     # Number of resources (generators, storage, DR, and DERs)
    T = inputs["T"]     # Number of time steps (hours)

    # Power injected by each resource in each time step
    dfPower = DataFrame(Resource = inputs["RESOURCE_NAMES"],
        Zone = zones,
        AnnualSum = Array{Union{Missing, Float64}}(undef, G))
    power = value.(EP[:vP])
    if setup["ParameterScale"] == 1
        power *= ModelScalingFactor
    end
    dfPower.AnnualSum .= power * inputs["omega"]

    filepath = joinpath(path, "power.csv")
    if setup["WriteOutputs"] == "annual"
        write_annual(filepath, dfPower)
    else # setup["WriteOutputs"] == "full"
        write_fulltimeseries(filepath, power, dfPower)
        if setup["OutputFullTimeSeries"] == 1 & setup["TimeDomainReduction"] == 1
            T = size(power, 2)
            dfPower = hcat(dfPower, DataFrame(power, :auto))
            auxNew_Names = [Symbol("Resource");
                            Symbol("Zone");
                            Symbol("AnnualSum");
                            [Symbol("t$t") for t in 1:T]]
            rename!(dfPower, auxNew_Names)
            total = DataFrame(["Total" 0 sum(dfPower[!, :AnnualSum]) fill(0.0, (1, T))], auxNew_Names)
            total[!, 4:(T + 3)] .= sum(power, dims = 1)
            df_Power = vcat(dfPower, total)
            DFMatrix = Matrix(dftranspose(df_Power, true))
            DFnames = DFMatrix[1,:]

            FullTimeSeriesFolder = setup["OutputFullTimeSeriesFolder"]
            output_path = joinpath(path, FullTimeSeriesFolder)
            dfOut_full = full_time_series_reconstruction(
                path, setup, dftranspose(df_Power, false), DFnames)
            CSV.write(joinpath(output_path, "power.csv"), dfOut_full, writeheader = false)
            println("Writing Full Time Series for Power")
        end
    end

    return dfPower #Shouldn't this be return nothing
end
